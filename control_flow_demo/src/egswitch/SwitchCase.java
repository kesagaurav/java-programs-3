package egswitch;

public class SwitchCase {

	public static void main(String[] args) {
		int ch = 2;
		switch (ch) {
		case 3:
			System.out.println("Wednesday");
			break;
		case 1:
			System.out.println("Monday");
			break;
		default:
			System.out.println("Its some other day");
			break;
		case 2:
			System.out.println("Tuesday");
			break;

		}

	}

}
